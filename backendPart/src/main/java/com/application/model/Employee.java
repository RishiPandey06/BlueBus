package com.application.model;

public class Employee {
	private int emp_id;
	private String emp_name;
	private int log_role;
	private String designation;
	private int sv_emp_id;
	private String emp_password;
	private int project_id;
	private String phone_no;
	private String mail_id;
	
	public int getEmp_id() {
		return emp_id;
	}
	public void setEmp_id(int emp_id) {
		this.emp_id = emp_id;
	}
	public String getEmp_name() {
		return emp_name;
	}
	public void setEmp_name(String emp_name) {
		this.emp_name = emp_name;
	}
	public int getLog_role() {
		return log_role;
	}
	public void setLog_role(int log_role) {
		this.log_role = log_role;
	}
	public String getDesignation() {
		return designation;
	}
	public void setDesignation(String designation) {
		this.designation = designation;
	}
	public int getSv_emp_id() {
		return sv_emp_id;
	}
	public void setSv_emp_id(int sv_emp_id) {
		this.sv_emp_id = sv_emp_id;
	}
	public String getEmp_password() {
		return emp_password;
	}
	public void setEmp_password(String emp_password) {
		this.emp_password = emp_password;
	}
	public int getProject_id() {
		return project_id;
	}
	public void setProject_id(int project_id) {
		this.project_id = project_id;
	}
	public String getPhone_no() {
		return phone_no;
	}
	public void setPhone_no(String phone_no) {
		this.phone_no = phone_no;
	}
	public String getMail_id() {
		return mail_id;
	}
	public void setMail_id(String mail_id) {
		this.mail_id = mail_id;
	}
	
}
